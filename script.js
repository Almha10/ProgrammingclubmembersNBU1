function showResult() {
    const year = document.getElementById('year-input').value;
    const result = document.getElementById('result');

    // Validate the year input
    if (year >= 1900 && year <= 2024) {
        // Check if there's an achievement for the entered year
        if (achievements[year]) {
            result.innerHTML = `في سنة ${year}: ${achievements[year]}`;
        } else {
            result.innerHTML = `في سنة ${year}، لم يتم تسجيل أي إنجاز محدد لهذه السنة في البيانات المتاحة.`;
        }
    } else {
        result.innerHTML = "الرجاء إدخال سنة صحيحة بين 1900 و 2024.";
    }
    result.classList.add("fade-in");
    setTimeout(() => {
        result.classList.remove("fade-in");
    }, 1000); 
}
